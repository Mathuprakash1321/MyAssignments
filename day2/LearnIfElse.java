package week1.day2;

public class LearnIfElse {

	public static void main(String[] args) {
		
		int mark=360;
		
		if (mark>400)
		{
			System.out.println("if condition satisfied");
		}
		else if(mark>320) {
			System.out.println("else if condition satisfied ");
		}
		else {
			System.out.println("condition fails");
		}

	}

}
